
package typecasting;

public class TypeCasting {

    public static void main(String[] args) {
        double d=100.04;
        long l=(long)d;
        int i=(int)l;
        
        System.out.println("Value of double : "+d);
        System.out.println("Value of long : "+l);
        System.out.println("Value of int : "+i);
    }
    
}
